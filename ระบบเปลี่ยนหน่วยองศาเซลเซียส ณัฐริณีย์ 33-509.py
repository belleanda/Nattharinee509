print('นี่คือระบบเปลี่ยนหน่วยองศาเซลเซียสเป็นองศาฟาเรนไฮต์และเคลวิน')
while True:
    try:
        C = float(input("ค่าองศาเซลเซียส: "))
        F = (9 / 5) * C + 32  
        K = C + 273.15 
        print(">>>จะได้ค่าองศาฟาเรนไฮต์เท่ากับ:", F)
        print(">>>จะได้ค่าเคลวินเท่ากับ:", K)
        break
    except ValueError:
        print('>>>>> คุณต้องกรอกค่าองศาเป็นตัวเลขเท่านั้น ไม่ต้องใส่หน่วยมา')